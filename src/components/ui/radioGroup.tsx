type Plan = {
  name: string;
  value: number;
  icon: JSX.Element;
};

export default function RadioButton({ walletChosen, value }: { walletChosen: string, value: number }) {
  const [plans, setPlans] = useState<Plan[]>([]);

  const [selected, setSelected] = useState(plans[0])


  useEffect(() => {
    switch (walletChosen) {
      case "avalanche":
        allplans.AvalanchePlans[0].value = value
        allplans.AvalanchePlans[1].value = usdcToAvax(value)
        setPlans(allplans.AvalanchePlans);
        break;
      case "Ethereum":
        allplans.ethPlans[0].value = value
        allplans.ethPlans[1].value = usdcToEth(value)
        setPlans(allplans.ethPlans);
        break;
      case "Moonbeam":
        allplans.moonBeamPlans[0].value = value
        setPlans(allplans.moonBeamPlans);
        break;
      case "Polygon":
        allplans.polygonPlans[0].value = value
        setPlans(allplans.polygonPlans);
        break;
      case "Metis":
        allplans.metisPlans[0].value = value
        setPlans(allplans.metisPlans);
        break;
      case "ZkSync":
        allplans.zkSyncPlans[0].value = value
        allplans.zkSyncPlans[1].value = usdcToWeth(value)
        setPlans(allplans.zkSyncPlans);
        break;
      case "Morph":
        allplans.morphPlans[0].value = value
        setPlans(allplans.morphPlans);
        break;
      case "Polygon_Zkevm":
        allplans.Polygon_Zkevm[0].value = value
        setPlans(allplans.Polygon_Zkevm);
        break;
      case "Base":
        allplans.Base[0].value = value
        setPlans(allplans.Base);
        break;
      default:
        setPlans([]);
    }
  }, [walletChosen]);

  return (
    <div className="w-full ">
      <div className="mx-auto w-full max-w-md">
        <RadioGroup value={selected} onChange={setSelected} aria-label="Server size" className="gap-2 flex">
          {plans.map((plan) => (
            <Radio
              key={plan.name}
              value={plan}
              className="group relative flex cursor-pointer rounded-lg bg-white/5 py-2 px-2 text-white shadow-md transition focus:outline-none data-[focus]:outline-1 data-[focus]:outline-white data-[checked]:bg-white/10"
            >
              <div className="flex gap-1 text-sm w-full items-center justify-between">
                {plan.icon}
                <p>{plan.value}</p>
                <p className="font-semibold  text-white">{plan.name}</p>
                <CheckCircleIcon className="size-4  fill-white bg-[rgb(192,77,188)] rounded-full   opacity-0 transition group-data-[checked]:opacity-100" />
              </div>
            </Radio>
          ))}
        </RadioGroup>
      </div>
    </div>
  )
}